class Solution
{
    public:
    //Function to find list of all words possible by pressing given numbers.
    vector<string> ans;
    map<int, string> mp;
    void initialize(){
        mp[2] = "abc";
        mp[3] = "def";
        mp[4] = "ghi";
        mp[5] = "jkl";
        mp[6] = "mno";
        mp[7] = "pqrs";
        mp[8] = "tuv";
        mp[9] = "wxyz";
    }
    void solve(int a[], int index, int n, string temp){
        if(index >= n){
            ans.push_back(temp);
            return;
        }
        for(auto it : mp[a[index]]){
            solve(a , index+1 , n , temp+it);
        }
        return;
    }
    vector<string> possibleWords(int a[], int N)
    {
        //Your code here
        string temp="";
        initialize();
        solve(a , 0 , N , temp);
        return ans;
    }
};


