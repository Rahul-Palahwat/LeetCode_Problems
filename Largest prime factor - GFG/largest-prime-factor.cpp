class Solution{
public: 
    long long int largestPrimeFactor(int N){
        // code here
        int ans = 2;
        int c = 2;
        while(N>1){
            if(N%c == 0){
                ans = c;
                N/=c;
            }else{
                c++;
            }
        }
        return ans;
    }
};

