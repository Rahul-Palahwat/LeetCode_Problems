class Solution {
  public:
    vector<string> CamelCase(int N, vector<string> Dictionary, string Pattern) {
        // code here
        vector<string> ans;
        ans.push_back("-1");
        map<string , vector<string>> mp;
        for(auto it: Dictionary){
            string temp = "";
            for(auto i:it){
                if((i-'Z') <= 0){
                    temp+=i;
                    mp[temp].push_back(it);
                }
            }
        }
        if(mp.find(Pattern) != mp.end()){
            return mp[Pattern];
        }
        return ans;
    }
};

